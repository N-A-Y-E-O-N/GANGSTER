package gang;

public class DTOcounsel {
	private String coun_code;
	private String coun_name;
	private String coun_date;
	private String coun_tel;
	private String coun_mail;
	private String coun_title;
	private String coun_content;
	private String coun_done;

	
	public String getCoun_code() {
		return coun_code;
	}
	public void setCoun_code(String coun_code) {
		this.coun_code = coun_code;
	}
	public String getCoun_name() {
		return coun_name;
	}
	public void setCoun_name(String coun_name) {
		this.coun_name = coun_name;
	}
	public String getCoun_date() {
		return coun_date;
	}
	public void setCoun_date(String coun_date) {
		this.coun_date = coun_date;
	}
	public String getCoun_tel() {
		return coun_tel;
	}
	public void setCoun_tel(String coun_tel) {
		this.coun_tel = coun_tel;
	}
	public String getCoun_mail() {
		return coun_mail;
	}
	public void setCoun_mail(String coun_mail) {
		this.coun_mail = coun_mail;
	}
	public String getCoun_title() {
		return coun_title;
	}
	public void setCoun_title(String coun_title) {
		this.coun_title = coun_title;
	}
	public String getCoun_content() {
		return coun_content;
	}
	public void setCoun_content(String coun_content) {
		this.coun_content = coun_content;
	}

	public String getCoun_done() {
		return coun_done;
	}
	public void setCoun_done(String coun_done) {
		this.coun_done = coun_done;
	}
	

}
