package gang;

public class DTOcomments {
	private String comm_code;
	private String mang_code;
	private String id;
	private String comm_comment;
	private String comm_date; 
	
	
	public String getComm_code() {
		return comm_code;
	}
	public void setComm_code(String comm_code) {
		this.comm_code = comm_code;
	}
	public String getMang_code() {
		return mang_code;
	}
	public void setMang_code(String mang_code) {
		this.mang_code = mang_code;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getComm_comment() {
		return comm_comment;
	}
	public void setComm_comment(String comm_comment) {
		this.comm_comment = comm_comment;
	}
	public String getComm_date() {
		return comm_date;
	}
	public void setComm_date(String comm_date) {
		this.comm_date = comm_date;
	}
	

	
}
